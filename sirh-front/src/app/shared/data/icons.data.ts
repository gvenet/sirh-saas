/**
 * Helper function to get icons as array for the picker
 * Only returns icons that have a category defined
 */
export function getIconsForPicker(): IconOption[] {
    return Object.entries(ICONS_DATA)
        .filter(([_, data]) => data.category)
        .map(([name, data]) => ({
            name,
            svg: data.svg,
            viewBox: data.viewBox,
            category: data.category!
        }));
}

/**
 * Helper function to get IconData by name
 */
export function getIconData(name: string): IconData {
    const lowerName = name?.toLowerCase();
    const icon = ICONS_DATA[lowerName];
    if (icon) {
        return { svg: icon.svg, viewBox: icon.viewBox };
    }
    return DEFAULT_ICON;
}
